<template>
  <section class="container">
    <h1>Demo examples of vue-chartsjs</h1>
    <div class="columns">
      <div class="column">
        <h3>Line Chart</h3>
        <line-chart></line-chart>
      </div>
      <div class="column">
        <h3>Bar Chart</h3>
        <bar-chart></bar-chart>
      </div>
    </div>
    <div class="columns">
      <div class="column">
        <h3>Bubble Chart</h3>
        <bubble-chart></bubble-chart>
      </div>
      <div class="column">
        <h3>Reactivity - Live update upon change in datasets</h3>
        <reactive :chart-data="dataCollection"></reactive>
        <button class="button is-primary" @click="fillData()">Randomize</button>
      </div>
    </div>
  </section>
</template>
<script>
import LineChart from "../components/LineChart.vue";
import BarChart from "../components/BarChart.vue";
import BubbleChart from "../components/BubbleChart.vue";
import Reactive from "../components/Reactive.vue";
export default {
  components: { LineChart, BarChart, BubbleChart, Reactive },
  name: "vue-charts-js",
  data() {
    return {
      // instantiating dataCollection with null
      dataCollection: null,
    };
  },
  created() {
    // anytime the vue instance if created, call the fillData() function
    this.fillData();
  },
  methods: {
    fillData() {
      this.dataCollection = {
        // Data for the y-axis of the chart
        labels: [
          "January",
          "February",
          "March",
          "April",
          "May",
          "June",
          "July",
          "August",
          "September",
          "October",
          "November",
          "December",
        ],
        datasets: [
          {
            label: "Data One",
            backgroundColor: "#f87979",
            // Data for the x-axis of the chart
            data: [
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
              this.getRandomInt(),
            ],
          },
        ],
      };
    },
    getRandomInt() {
      // JS function to generate numbers to be used for the chart
      return Math.floor(Math.random() * (50 - 5 + 1)) + 5;
    },
  },
};
</script>